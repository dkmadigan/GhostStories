package games.ghoststories.views.title;

import android.content.Context;
import android.util.AttributeSet;
import android.widget.LinearLayout;

/**
 * The title screen view.
 */
public class TitleScreen extends LinearLayout {

   /**
    * Constructor
    * @param pContext
    */
   public TitleScreen(Context pContext) {
      super(pContext);
   }

   /**
    * Constructor
    * @param pContext
    * @param pAttrs
    */
   public TitleScreen(Context pContext, AttributeSet pAttrs) {
      super(pContext, pAttrs);
   }

   /**
    * Constructor
    * @param pContext
    * @param pAttrs
    * @param pDefStyle
    */
   public TitleScreen(Context pContext, AttributeSet pAttrs, int pDefStyle) {
      super(pContext, pAttrs, pDefStyle);
   }
}
